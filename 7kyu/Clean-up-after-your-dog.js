//❓Вы открыли для себя божественное удовольствие, которое дарит владение собакой и садом. Теперь пора убрать весь этот хлам! :D
//
// Дана двумерная матрица, представляющая ваш сад. Вам необходимо найти и собрать все собачьи экскременты, обозначенные символом '@'.
//
// Вам также будет указано количество доступных вам сумок ( bags) и вместимость сумки ( cap). Если сумок нет, bagsто вы ничего не сможете взять, поэтому можете игнорировать cap.
//
// Вам нужно выяснить, хватит ли у вас места, чтобы собрать весь мусор и снова навести порядок в саду.
//
// Если вы это сделаете, вернитесь 'Clean', в противном случае вернитесь 'Cr@p'.
//
// Однако будьте осторожны — если ваша собака находится там ( 'D'), она очень реагирует на то, что за ней наблюдают. Если она там, вам нужно вернуться 'Dog!!'.
//
// Например:
//
// bags = 2
// cap = 2
// x (or garden) =
// [[ _ , _ , _ , _ , _ , _ ],
//  [ _ , _ , _ , _ , @ , _ ],
//  [ @ , _ , _ , _ , _ , _ ]]

// ✅ SOLUTION:
function crap(garden, bags, cap) {
    const hasDog = garden.some(row => row.includes('D'));
    if (hasDog) return "Dog!!";

    const crapCount = garden.flat().filter(cell => cell === '@').length;

    if (bags === 0) return crapCount === 0 ? "Clean" : "Cr@p";

    const totalCapacity = bags * cap;

    return totalCapacity >= crapCount ? "Clean" : "Cr@p";
}

