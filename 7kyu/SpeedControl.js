//❓В машине Джона GPS-навигатор каждую sсекунду записывает пройденное расстояние от начальной точки (расстояния измеряются в произвольной, но постоянной единице измерения). Например, ниже приведена часть записи, содержащая s = 15:
//
// x = [0.0, 0.19, 0.5, 0.75, 1.0, 1.25, 1.5, 1.75, 2.0, 2.25]
// Разделы следующие:
//
// 0.0-0.19, 0.19-0.5, 0.5-0.75, 0.75-1.0, 1.0-1.25, 1.25-1.50, 1.5-1.75, 1.75-2.0, 2.0-2.25
// Мы можем рассчитать среднюю почасовую скорость Джона на каждом участке и получим:
//
// [45.6, 74.4, 60.0, 60.0, 60.0, 60.0, 60.0, 60.0, 60.0]
// Дано значение x s, и xзадача состоит в том, чтобы вернуть в виде целого числа значение *floor*максимальной средней скорости в час, полученное на участках x. Если x меньше или равно 1, вернуть значение, 0так как автомобиль не двигался.
//
// Пример:
// Исходя из приведенных выше данных, ваша функция gps(s, x)должна вернуть...74
//
// Примечание
// При использовании плавающих резервов результаты могут зависеть от порядка выполнения операций. Для расчета почасовой скорости можно использовать:
//
//  (3600 * delta_distance) / s.
//
// Удачного программирования!

// ✅ SOLUTION:
function gps(s, x) {
    if (x.length <= 1) return 0;

    let maxSpeed = 0;

    for (let i = 1; i < x.length; i++) {
        const delta = x[i] - x[i - 1];
        const speed = (3600 * delta) / s;
        if (speed > maxSpeed) {
            maxSpeed = speed;
        }
    }

    return Math.floor(maxSpeed);
}
