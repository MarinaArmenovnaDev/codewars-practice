//❓Сильное число — это число, сумма факториалов цифр которого равна самому числу.
//
//    Например, число 145 является сильным, потому что 1! + 4! + 5! = 1 + 24 + 120 = 145.
//
//    Задача
//    Получив положительное число, определите, является ли оно сильным или нет, и верните "STRONG!!!!"либо "Not Strong !!".
//
//    Примеры
//    1Это сильное число, потому что 1! = 1, поэтому верните "STRONG!!!!".
//    123Это не очень сильное число, потому что 1! + 2! + 3! = 9оно не равно 123, поэтому верните "Not Strong !!".
//    145Это сильное число, потому что 1! + 4! + 5! = 1 + 24 + 120 = 145, поэтому верните "STRONG!!!!".
//    150Это не очень сильное число, потому что 1! + 5! + 0! = 122оно не равно 150, поэтому верните "Not Strong !!".


//✅ SOLUTION:

function strong(n) {
   // Предварительно вычисленные факториалы для цифр 0-9
   const factorials = [1, 1, 2, 6, 24, 120, 720, 5040, 40320, 362880];

   const digits = String(n).split('');

   let sum = 0;
   for (let digit of digits) {
      sum += factorials[parseInt(digit)];
   }
   return sum === n ? "STRONG!!!!" : "Not Strong !!";

}

console.log(strong(1));
