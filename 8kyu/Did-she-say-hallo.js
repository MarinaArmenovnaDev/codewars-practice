// Напишите простую функцию для проверки того, содержит ли строка слово "hallo" на разных языках.
//
// Вот языки тех людей, с которыми вы, возможно, познакомились накануне вечером:
//
// привет - английский
// ciao - итальянский
// салют - французский
// привет - немецкий
// хола - испанский
// ahoj - Чешская Республика
// чеш - польский
// Примечания
//
// Можно предположить, что входные данные представляют собой строку.
// Чтобы это оставалось упражнением для начинающих, вам не нужно проверять, является ли приветствие подмножеством слова ( Hallowenпроверка пройдена).
// Для прохождения тестов функция должна быть нечувствительна к регистру.

//✅ SOLUTION:
function validateHello(greetings) {
    const valid = ["hello", "ciao", "salut", "hallo", "hola", "ahoj", "czesc"];
    const lowerGreeting = greetings.toLowerCase();

    return valid.some(word => lowerGreeting.includes(word));
}


