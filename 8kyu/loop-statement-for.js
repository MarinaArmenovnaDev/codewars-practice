// Как видите, в скобках три части, разделённые кавычками ;. Первая часть var sum=0,num=1— это инициализация. Она выполняется перед началом цикла. Вторая часть — num<=100это условное выражение. Условное выражение проверяется перед каждым проходом цикла. Если значение выражения ложно, цикл завершается. Третья часть num++выполняется после блока кода и обычно используется для увеличения и уменьшения значений переменных.
