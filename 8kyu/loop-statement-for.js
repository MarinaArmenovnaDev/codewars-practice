// Задача
// Функция pickItпринимает один параметр, arrкоторый представляет собой массив чисел. Для обхода массива необходимо arrиспользовать forцикл. Если элемент нечётный, его нужно добавить в oddмассив, а если чётный — также в evenмассив.
//
// Я определил два массива oddв evenфункции, а также написал оператор return. Ваша задача — написать forцикл.

//✅ SOLUTION:
function pickIt(arr){
    let odd = [], even = [];
  for(let i = 0; i < arr.length; i++){
      if(arr[i]%2 === 0){
          even.push(arr[i]);
      }else{
          odd.push(arr[i]);
      }
  }
    return [odd,even];
}

console.log(pickIt([1,2,3,4,6,7,8]));
